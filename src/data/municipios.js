// Função para buscar município por código IBGE
export function getMunicipioByIbge(ibge) {
    return municipiosMS.find(m => m.ibge === ibge)
}

// Função para buscar município por nome
export function getMunicipioByNome(nome) {
    return municipiosMS.find(m => 
        m.nome.toLowerCase().includes(nome.toLowerCase())
    )
}

// Função para listar municípios de uma macrorregião
export function getMunicipiosByMacrorregiao(macrorregiao) {
    return municipiosMS.filter(m => m.macrorregiao === macrorregiao)
}

// Função para listar municípios de uma microrregião
export function getMunicipiosByMicrorregiao(microrregiao) {
    return municipiosMS.filter(m => m.microrregiao === microrregiao)
}

// Função para obter lista de macrorregiões
export function getMacrorregioes() {
    return [...new Set(municipiosMS.map(m => m.macrorregiao))].sort()
}

// Função para obter lista de microrregiões
export function getMicrorrregioes() {
    return [...new Set(municipiosMS.map(m => m.microrregiao))].sort()
}

// População total do estado
export function getPopulacaoTotalMS() {
    return municipiosMS.reduce((total, m) => total + m.populacao, 0)
}

// Função para busca por texto (nome ou código)
export function searchMunicipios(query) {
    if (!query) return municipiosMS
    
    const searchTerm = query.toLowerCase()
    return municipiosMS.filter(m => 
        m.nome.toLowerCase().includes(searchTerm) ||
        m.ibge.includes(searchTerm) ||
        m.macrorregiao.toLowerCase().includes(searchTerm) ||
        m.microrregiao.toLowerCase().includes(searchTerm)
    )
}

// Função para ordenar municípios
export function sortMunicipios(municipios, criterio = 'nome') {
    return [...municipios].sort((a, b) => {
        switch (criterio) {
            case 'nome':
                return a.nome.localeCompare(b.nome)
            case 'populacao':
                return b.populacao - a.populacao
            case 'ibge':
                return a.ibge.localeCompare(b.ibge)
            case 'macrorregiao':
                return a.macrorregiao.localeCompare(b.macrorregiao) || a.nome.localeCompare(b.nome)
            default:
                return a.nome.localeCompare(b.nome)
        }
    })
}